function pow(i, j) {
    let result = 1;
    
    // Перевірка на 0-ту ступінь
    if (j === 0) {
        return 1;
    }
    
    // Визначення знаку
    const sign = j < 0 ? -1 : 1;
    
    // Перетворюємо ступінь у додатнє число для спрощення обчислень
    j *= sign;
    
    for (let a = 0; a < j; a++) {
        result *= i;
    }
    
    // Віддавання результату із врахуванням знаку
    return sign === 1 ? result : 1 / result;
}

// Приклад використання з завдання та ще парочка)
console.log(pow(2, 3)); // Виведе 8
console.log(pow(2, -3)); // Виведе 0.125 (2 в -3 степені = 1/(2^3))
console.log(pow(5, 0)); // Виведе 1 (будь-яке число в 0-й ступені = 1)
