// Ініціалізуємо змінну для зберігання суми
let сума = 0;

// Проходимося по всім числам від 1 до 100
for (let число = 1; число <= 100; число++) {
    // Перевіряємо, чи не кратне число 3
    if (число % 3 !== 0) {
        // Додаємо число до суми
        сума += число;
    }
}

// Виводимо суму на екран згідно завданню
console.log("Сума чисел від 1 до 100, які не кратні 3, дорівнює:", сума);
